function oper(n1, n2) {
    console.log('soma', n1 + n2)
    console.log('subtração', n1 - n2)
    console.log('multiplicação', n1 * n2)
    console.log('divisão', n1 / n2)
}
function oper2(n3, n4) {
    console.log(`soma:${n3 + n4} subtração:${n3 - n4} multiplicação:${n3 * n4} divisão:${n3 / n4}`)
}

oper(5, 6)
oper2(5, 6)